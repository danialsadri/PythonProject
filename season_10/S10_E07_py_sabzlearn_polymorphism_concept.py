# Polymorphism
# Poly ==> چندین | زیاد
# morphism ==> شکل یا ریخت

# subtype polymorphism (runtime) ==> یه کلاس پایه داریم و یه سری کلاس ها از اون ارث بری میکنن و متدی که اونجا مطرح شده برای خودشون شخصی سازی میکنن

# parametric polymorphism (overloading) ==> یه تابع دارم که اون کاری که داره انجام میده میتونه روی همه نوع ها همزمان اعمال بشه

# Ad hoc polymorphism ==> method overloading / operator overloading
# method overloading ==> چند تا متد داریم با اسم های یکسان ولی ورودی های مختلف
# operator overloading ==> یک عملگر داری که مشخصه چیه  یدونه عملگره ولی رفتار متفاوته

# coercion polymorphism (casting) ==> تبدیل نوع خودکار یعنی یک نوع به نوع دیگه تبدیل میشه


